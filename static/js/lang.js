function tBranch(name) {
    if (currentLang !== 'en' || !name) return name;
    const m = String(name).match(/^Şube\s*(\d+)$/i);
    if (m) return 'Branch ' + m[1];
    return String(name).replace(/^Şube\s*/i, 'Branch ');
}

const currentLang = localStorage.getItem('lang') || 'en';

function t(text) {
    if (currentLang === 'en' && dictionary[text]) return dictionary[text];
    return text;
}

function getLocale() {
    return currentLang === 'en' ? 'en-US' : 'tr-TR';
}

function tVisit(count) {
    if (currentLang === 'en') return `⭐ Visit #${count}`;
    return `⭐ Bu araç ${count}. kez geliyor!`;
}

function tPriceHint(list, final, pm) {
    if (currentLang === 'en') {
        if (pm === 'nakit') return `(List ₺${list} → Cash ₺${final}, -₺${cashDiscountGlobal()} off)`;
        return `(Fixed: ₺${list})`;
    }
    if (pm === 'nakit') return `(Liste ₺${list} → Nakit ₺${final}, -₺${cashDiscountGlobal()} indirim)`;
    return `(Sabit: ₺${list})`;
}

function cashDiscountGlobal() {
    return typeof window !== 'undefined' && window.cashDiscount ? window.cashDiscount : 100;
}

function tCashDiscountNote(washCount, discount) {
    if (currentLang === 'en') return `(Cash: ${washCount} wash × ₺${discount} = ₺${washCount * discount} discount)`;
    return `(Nakit: ${washCount} yıkama × ₺${discount} = ₺${washCount * discount} indirim)`;
}

function tWashType(wt) {
    return t(wt) || wt;
}

function tPaymentPm(pm) {
    const map = { nakit: 'NAKİT', kk: 'KREDİ KARTI', havale: 'HAVALE', bekliyor: 'BEKLİYOR' };
    return t(map[pm] || pm);
}

function changeLang(lang) {
    localStorage.setItem('lang', lang);
    location.reload();
}

function applyPageLang() {
    document.documentElement.lang = currentLang;
    if (currentLang !== 'en') return;

    document.querySelectorAll('[data-lang]').forEach(el => {
        const key = el.getAttribute('data-lang');
        if (dictionary[key]) el.textContent = dictionary[key];
    });

    document.querySelectorAll('option').forEach(opt => {
        const key = opt.textContent.trim();
        if (dictionary[key]) opt.textContent = dictionary[key];
        else if (opt.value && dictionary[opt.value]) opt.textContent = dictionary[opt.value];
    });

    document.querySelectorAll('label').forEach(el => {
        const first = el.childNodes[0];
        if (first && first.nodeType === Node.TEXT_NODE) {
            const trimmed = first.textContent.trim();
            if (trimmed && dictionary[trimmed]) first.textContent = first.textContent.replace(trimmed, dictionary[trimmed]);
        }
    });

    document.querySelectorAll('[placeholder]').forEach(el => {
        if (dictionary[el.placeholder]) el.placeholder = dictionary[el.placeholder];
    });

    document.querySelectorAll('[title]').forEach(el => {
        if (dictionary[el.title]) el.title = dictionary[el.title];
    });

    const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, null, false);
    const nodes = [];
    let node;
    while (node = walker.nextNode()) nodes.push(node);
    nodes.forEach(n => {
        const trimmed = n.nodeValue.trim();
        if (trimmed && dictionary[trimmed]) {
            n.nodeValue = n.nodeValue.replace(trimmed, dictionary[trimmed]);
        }
    });
}

document.addEventListener('DOMContentLoaded', () => {
    applyPageLang();
    const langSwitch = document.getElementById('lang-switch');
    if (langSwitch) langSwitch.value = currentLang;
    if (typeof restoreLastPayment === 'function') restoreLastPayment();
});
